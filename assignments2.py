##This programme is to calculate the area and circumference of a circle and also calculate the surface area and volume of a sphere.
##To calculate the area of a circle,
r=15
PI=3.14
Area=(PI*(r**2))
print(f"The area of the circle is {Area}cm(square)")
##To calculate the circumference of a circle,
circumference=2*PI*r
print(f"The circumference of the circle is {circumference}cm")
##To calculate the surface area of a sphere,
Area=4*PI*r**2
print(f"The surface area of the sphere is {Area}cm(square)")
##To calculate the volume of a sphere,
Volume=4/3*(PI*r**3)
print(f"The volume of the sphere is {Volume}cm(cube)")
